// === Modal logic (igual que Barcelona) ===
const modal = document.getElementById('modal');
const modalInfo = document.getElementById('modal-info');
const modalClose = document.getElementById('modalClose');
if (modalClose) { modalClose.addEventListener('click', cerrarModal); }

function mostrarModal(key) {
  const info = data[key];
  if (!info) return;
  const playersHTML = (info.players || []).map(p => `
    <div style="display:flex;flex-direction:column;align-items:center;margin:0 10px;">
      <img src="${p.img || 'img/placeholder-player.png'}" 
           alt="${p.name}" 
           title="${p.name}" 
           style="width:70px;height:90px;object-fit:cover;border-radius:8px;box-shadow:0 2px 8px #0002;">
      <span style="font-size:0.95em;margin-top:4px;color:#333;font-weight:500;">${p.name}</span>
    </div>`).join("");
  modalInfo.innerHTML = `
    <div class="modal-info">
      <div class="top">
        <img class="jersey-large" src="${info.img}" alt="Camiseta ${info.season} - ${info.role}">
        <div class="details">
          <h3>${info.season} — ${info.role}</h3>
          <p><strong>Diseño:</strong> ${info.design}</p>
          <p><strong>Colores:</strong> ${info.colors}</p>
          <p><strong>Marca:</strong> ${info.brand}</p>
          <p><strong>Patrocinador:</strong> ${info.sponsor}</p>
          <p><strong>Competiciones:</strong> ${info.competitions}</p>
          <div class="palmares"><strong>Palmarés:</strong> ${info.trophies || "Ninguno"}</div>
        </div>
      </div>
      <p><strong>Breve historia:</strong> ${info.description}</p>
      <div>
        <p><strong>Jugadores icónicos:</strong></p>
        <div class="players">${playersHTML}</div>
      </div>
    </div>`;
  modal.style.display = 'block';
  modal.setAttribute('aria-hidden', 'false');
}

function cerrarModal() {
  modal.style.display = 'none';
  modal.setAttribute('aria-hidden', 'true');
}
window.addEventListener('click', e => { if (e.target === modal) cerrarModal(); });
window.addEventListener('keydown', e => { if (e.key === 'Escape') cerrarModal(); });
document.addEventListener('click', e => {
  const img = e.target.closest('img[data-key]');
  if (img) mostrarModal(img.getAttribute('data-key'));
});
